def memoryzat(f):
    """"Функция Мемоизация (кэширования)
    Данная функция позволяет:
    - Сохранять результаты вызовов декорируемой функции в словаре
    - При повторном вызове с теми же аргументами возвращать сохранённый результат,
      избегая повторных вычислений
    - Нормализовать порядок аргументов с помощью сортировки, чтобы перестановки
      аргументов считались одинаковым вызовом (для коммутативных операций)
    - Предоставлять доступ к словарю кэша через атрибут .s декорированной функции
    """
    s = {}
    def inner(*args):
        if args in s:
            print(f"Аргументы: {args}")
            r = s[args]
        else: 
            print(f"Аргументы: {args}")
            r = f(*args)
            k = tuple(sorted(args))
            s[k] = r
        return r
    inner.s = s
    return inner

@memoryzat
def dec(c, d, e):
    r = c*d*e
    return r

